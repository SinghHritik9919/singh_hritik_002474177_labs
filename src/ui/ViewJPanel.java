    private void btn_DeleteVitalsActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btn_DeleteVitalsActionPerformed
        // TODO add your handling code here:
        int selectedRowIndex = tbl_Vitals.getSelectedRow();
        
        if(selectedRowIndex < 0){
            
            JOptionPane.showMessageDialog(this, "Please select a row to delete.");
            
            return;
        }
        
        DefaultTableModel model = (DefaultTableModel)tbl_Vitals.getModel();
        VitalSigns selectedVitals = (VitalSigns) model.getValueAt(selectedRowIndex, 0);
        
        history.deleteVitals(selectedVitals);
        
        JOptionPane.showMessageDialog(this, "Vitals Deleted");
        
        populateTable();
        
    }//GEN-LAST:event_btn_DeleteVitalsActionPerformed

