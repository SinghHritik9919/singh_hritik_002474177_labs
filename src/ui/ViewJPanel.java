    private void populateTable() {
        DefaultTableModel model = (DefaultTableModel)tbl_Vitals.getModel();
        model.setRowCount(0);
        for(VitalSigns vs : history.getHistory()){
        
        
            Object[] row = new Object[3];
            row[0] = vs;
            row[1] = vs.getTemprature();
            row[2] = vs.getPulse();
            
            model.addRow(row);
        
        }
        
        
        
        
       
    }
